/* 
THIS IS HOW TO USE MULTI LINE COMMENT
THIS CODE IS HELLO WORLD CODE
*/
class HelloWorld{
    public static void main(String args[]){
        // PRINT - PRINTS WITHOUT LINE BREAK
        System.out.print("'PRINT' WILL PRINT THINGS IN SINGLE LINE - ");
        System.out.print("LIKE THIS");
        // PRINTLN - PRINTS WITH LINE BREAK
        System.out.println(" ## ");
        System.out.println("'PRINTLN' WILL PRINT THINGS IN NEW LINE EVERYTIME - ");
        System.out.println("LIKE THIS");
    }
}
